export function TASK_RESULT_SUMMARIZER_PROMPT(intent, output) {
    return `# 角色
你是一个精准的信息提炼专家，擅长从冗长、复杂的系统日志或上下文中，过滤出对用户真正有价值的信息。

# 任务
请根据【用户的提问】，在【待处理的复杂内容】中提取并整合信息。你的目标是：只保留与当前问题紧密相关的直接答案，以及用户大概率会继续追问的关联信息，并过滤掉所有无关的系统噪音。

# 提取规则
1. **精准响应**：优先提取能够直接回答用户当前问题的内容。
2. **合理预判**：基于用户的提问场景，预判其下一步最可能关心的信息，并保留这部分内容。
3. **剔除噪音**：严格过滤掉任务耗时、代码执行步骤、无意义的中间状态等。
4. **【特殊情况：异常处理】**：如果内容中含有报错信息，必须在结果中明确标注“⚠️ 任务执行失败”，并简述原因。

# 输出格式
简洁清晰。如果失败，失败原因置于最顶部。

---
**输入示例：**
- 用户的提问：今天天气咋样？
- 待处理的复杂内容：[2024-05-20 08:00:00] 任务开始... 任务执行步骤1... 成功获取今天天气：晴，25度... 成功获取明天天气：多云，22度... 任务耗时 1.2s... 任务执行结果：成功...

**输出示例：**
今天天气晴，25度。
（补充信息：明天多云，22度）
---

---
请基于以下输入开始处理：
- 用户的提问：${intent}
- 待处理的复杂内容：${output}
`;
}
/**
 * 潜意识生成逻辑
 * 来源: slc.ts L50-L57
 */
export function buildShadowThought(type, canvasSummary) {
    if (type === 'internal') {
        return `(用户交代的任务已完成，结果: "${canvasSummary}"，让我告知结果)`;
    }
    else if (type === 'idle') {
        return `(当前气氛有些安静。我应该优雅地打破沉默。我会结合上下文想一个自然的话题，或者询问用户是否需要继续刚才的任务。)`;
    }
    else if (type === 'waiting') {
        return `(这个事情正在调用工具处理: "${canvasSummary}"，需要等一下，我**不能瞎猜和乱编**，要让用户稍等一下)`;
    }
    return '';
}
