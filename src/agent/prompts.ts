/**
 * [V3.3.0] 潜意识 (Shadow Thought) 生成逻辑
 * 职责：缝合画布状态到回复前缀中
 */
export type ShadowThoughtType =
    | 'idle'               // [V3.6.0] 闲置唤醒：用户沉默过久时触发，用于主动发起话题
    | 'chat'               // [V3.6.4] 基于画布问答：命中画布已知知识，引导模型基于事实回答
    | 'polishing'          // [V3.6.4] 任务结果润色：逻辑引擎决定无需工具且有直答内容时触发
    | 'PROGRESS_REPORT'    // [V3.6.18] 进度同步：异步长耗时任务的中期进度播报
    | 'RESULT_DELIVERY';   // [V3.6.17] 结果交付：异步任务完成或报错时的主动汇报

export function buildShadowThought(type: ShadowThoughtType, canvasSummary: string): string {
    // [V3.4.4] 集约化 ASR 纠错处理逻辑
    const isAsrCorrection = canvasSummary && canvasSummary.includes('ASR 纠错情报');

    if (isAsrCorrection) {
        // [V3.4.6] 针对 ASR：仅 Chat 模式才作为 Pre-fill 返回，且不再重复包装
        return type === 'chat' ? canvasSummary : '';
    }

    switch (type) {
        case 'PROGRESS_REPORT':
            return `(任务 "${canvasSummary}" 正在由外部工具执行，我现在不能直接给出答案，需要让用户等一会)`;
        case 'RESULT_DELIVERY':
            return `(后台任务已完成，最新进展是 "${canvasSummary}"，让我告知结果)`;
        case 'idle':
            return `(有一会没有说话了，我来打个招呼。)`;
        case 'chat':
            return `(询问的信息在"${canvasSummary}"之中，我以此作为依据回应。)`;
        case 'polishing':
            return `(这个任务不需要额外调用工具处理，可以直接参考"${canvasSummary}"，我来纠正一下)`;
        default:
            return '';
    }
}

